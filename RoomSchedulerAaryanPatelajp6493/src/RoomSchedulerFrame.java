    private void reserveSubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_reserveSubmitButtonActionPerformed
        // TODO add your handling code here:
        String faculty = String.valueOf(reserveFacultyComboBox.getSelectedItem());
        ArrayList<Date> y = Dates.getAllDates();
        Date date = y.get(reserveDateComboBox.getSelectedIndex());
        int seatsRequested = Integer.parseInt(reserveSeatsTextField.getText());
        if(ReservationQueries.addReservationEntry(faculty, date, seatsRequested)){
            String roomName;
            roomName = ReservationQueries.roomFinder(faculty, date);
            reserveRoomTextArea.setText("you are assigned a room " + roomName);
        }
        else{
            WaitlistEntry wle = new WaitlistEntry(faculty, date, seatsRequested);
            WaitlistQueries.addWaitlistEntry(wle);
            reserveRoomTextArea.setText("Sorry! there is no room available. You are in waitlist");
        }


    }//GEN-LAST:event_reserveSubmitButtonActionPerformed

