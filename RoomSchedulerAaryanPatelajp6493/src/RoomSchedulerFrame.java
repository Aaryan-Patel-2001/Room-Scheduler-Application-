    private void facultyButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_facultyButtonActionPerformed
        // TODO add your handling code here:
        ReservationsTextArea.setText("");
        String fac = facultyComboBox.getSelectedItem().toString();
        ArrayList<ReservationEntry> ry = ReservationQueries.getReservationsByFaculty(fac);
        ArrayList<WaitlistEntry> wy = WaitlistQueries.getWaitlistByFaculty(fac);
        ReservationsTextArea.append("Reservations:");
        for(ReservationEntry rt:ry){
            String swf = String.format("Room %s on date %s", rt.getRoom(), rt.getDate());
            ReservationsTextArea.append("\n");
            ReservationsTextArea.append(swf);
        }
        ReservationsTextArea.append("\nWaitlist:");
        for(WaitlistEntry tg:wy){
            String ybf = String.format("Seats requested: %s date: %s", tg.getSeats(), tg.getDate());
            ReservationsTextArea.append("\n");
            ReservationsTextArea.append(ybf);
        }
    }//GEN-LAST:event_facultyButtonActionPerformed

    private void cancelReservationsButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cancelReservationsButtonActionPerformed
        // TODO add your handling code here:
        reserveRoomTextArea.setText("");
        Date day = Date.valueOf(cancelDateComboBox.getSelectedItem().toString()); 
        String facName = cancelFacultyComboBox.getSelectedItem().toString();
        if(! ReservationQueries.isReservation(facName, day)){
            WaitlistQueries.deleteWaitlistEntry(facName, day);
            String removedFromWaitlist = String.format("%s is canceled from waitlist for %s", facName, day);
            reserveRoomTextArea.append(removedFromWaitlist);
        }
        else{
            ArrayList<String> facGotIn = ReservationQueries.deleteReservation(facName, day);
            String rtg = String.format("Reservation for %s on date %s has been canceled", facName, day);
            reserveRoomTextArea.append(rtg);
            for(String gotoff:facGotIn){
                reserveRoomTextArea.append("\n");
                reserveRoomTextArea.append(gotoff);
            }
        }
    }//GEN-LAST:event_cancelReservationsButtonActionPerformed

